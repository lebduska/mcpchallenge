-- Generic TypeScript MCP Client example for Chess
INSERT INTO code_snippets (id, title, description, language, code, challenge_id, is_public, is_curated, client_type, sort_order)
VALUES (
  'snippet-chess-generic-1',
  'TypeScript MCP Client',
  'Connect to the challenge using @modelcontextprotocol/sdk',
  'typescript',
  'import { Client } from "@modelcontextprotocol/sdk/client/index.js";
import { SSEClientTransport } from "@modelcontextprotocol/sdk/client/sse.js";

const transport = new SSEClientTransport(
  new URL("https://mcp.mcpchallenge.org/chess")
);

const client = new Client({
  name: "my-chess-ai",
  version: "1.0.0"
}, {
  capabilities: {}
});

await client.connect(transport);

// Start a new game as white
await client.callTool({
  name: "new_game",
  arguments: { color: "white" }
});

// Get the board state
const board = await client.callTool({ name: "get_board", arguments: {} });
console.log("Current position:", board);

// Get legal moves
const moves = await client.callTool({ name: "get_legal_moves", arguments: {} });
console.log("Legal moves:", moves);

// Make a move
await client.callTool({
  name: "make_move",
  arguments: { move: "e4" }
});',
  'chess',
  1,
  1,
  'generic',
  2
);
